// scripts/gen-skill-manifest.mjs - canonical machine-readable skill catalog
// (skill-manifest.json at repo root) and the generated AGENTS.md catalog block.
// Spec: docs/internal/release-plans/v2.27.0/spec_derived-surfaces.md (M-32, issue #201).
//
// Modes:
//   node scripts/gen-skill-manifest.mjs                  write skill-manifest.json
//   node scripts/gen-skill-manifest.mjs --check          fail if the manifest is stale
//   node scripts/gen-skill-manifest.mjs --agents         rewrite the AGENTS.md marker block
//   node scripts/gen-skill-manifest.mjs --agents --check fail if the AGENTS.md block is stale
//
// Frontmatter key mapping (Task 1 survey, 2026-06-12):
//   group:  metadata.phase present -> "phase" (with phase value); else metadata.classification
//   family: metadata.tool (sprint families; absent on standalone tools and non-tools)
//   move:   metadata.move (tool families only)
//   version: metadata.version (string, required); description: frontmatter description VERBATIM
//   (descriptions are never paraphrased here: the manifest mirrors the triggering surface, D-B)
//
// Formatting normalizations vs the historical hand-authored AGENTS.md (diff class (c),
// implementation plan Task 3): uniform entry shape with no `---` between entries inside a
// section, sprint-family entries ordered by their workshop sequence (MOVE_ORDER below),
// all other entries alphabetical. Output is byte-stable: no timestamps, sorted entries (D-F).
import { readFileSync, writeFileSync, readdirSync, existsSync } from 'node:fs';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { dirname, join } from 'node:path';
import yaml from 'js-yaml';

export const MARKER_START = '<!-- skills-catalog:start (generated by scripts/gen-skill-manifest.mjs --agents; edit skill frontmatter, not this block) -->';
export const MARKER_END = '<!-- skills-catalog:end -->';

const PHASES = ['discover', 'define', 'develop', 'deliver', 'measure', 'iterate'];
const FAMILY_ORDER = ['foundation-sprint', 'design-sprint'];
const MOVE_ORDER = {
  'foundation-sprint': ['readiness', 'brief', 'basics', 'differentiation', 'approach-options', 'magic-lenses', 'founding-hypothesis'],
  'design-sprint': ['readiness', 'brief', 'map-and-target', 'sketch', 'decide-and-storyboard', 'prototype-plan', 'test-and-score'],
};

// Hand-authored structural prose that lives INSIDE the generated block. This config is
// the single home for it; descriptions always come from frontmatter.
const FOUNDATION_NOTE = '> Meeting skills share a contract at [meeting-skills-contract](https://product-on-purpose.github.io/pm-skills/reference/skill-families/meeting-skills-contract/) governing frontmatter, file naming, go-mode behavior, and universal output requirements. Enforced by `scripts/validate-meeting-skills-family.sh`.';
const TOOL_INTRO = 'The `tool` classification represents named external methodologies composed of multiple skills working as a system. The first inhabitants are the Foundation Sprint and Design Sprint families plus a standalone decision tool.';
const FAMILY_INTROS = {
  'foundation-sprint': {
    title: 'Foundation Sprint Family',
    intro: 'The 7 `tool-foundation-sprint-*` skills implement Knapp and Zeratsky\'s two-day Foundation Sprint workshop. Family contract: [foundation-sprint-skills-contract](https://product-on-purpose.github.io/pm-skills/reference/skill-families/foundation-sprint-skills-contract/). Workflow: `_workflows/foundation-sprint.md`.',
  },
  'design-sprint': {
    title: 'Design Sprint Family',
    intro: 'The 7 `tool-design-sprint-*` skills implement Knapp, Zeratsky, and Kowitz\'s five-day Design Sprint workshop. Family contract: [design-sprint-skills-contract](https://product-on-purpose.github.io/pm-skills/reference/skill-families/design-sprint-skills-contract/). Workflow: `_workflows/design-sprint.md`. End-to-end FS-to-DS workflow: `_workflows/foundation-to-design.md`. User guide: [using-design-sprint](https://product-on-purpose.github.io/pm-skills/guides/using-design-sprint/). Concept doc: [design-sprint](https://product-on-purpose.github.io/pm-skills/concepts/design-sprint/).',
  },
};

function fail(msg) {
  throw new Error(msg);
}

/** Parse the YAML frontmatter of one SKILL.md; loud failure with path context. */
export function parseFrontmatter(text, path) {
  const m = /^---\r?\n([\s\S]*?)\r?\n---(\r?\n|$)/.exec(text);
  if (!m) fail(`${path}: no frontmatter block found`);
  let fm;
  try {
    fm = yaml.load(m[1]);
  } catch (e) {
    fail(`${path}: frontmatter does not parse as YAML (${e.message})`);
  }
  if (!fm || typeof fm !== 'object') fail(`${path}: frontmatter is not a mapping`);
  return fm;
}

/** Build one manifest entry from a skill directory. Loud failure on shape deviations. */
export function buildEntry(dirName, skillText, facts) {
  const path = `skills/${dirName}/SKILL.md`;
  const fm = parseFrontmatter(skillText, path);
  if (fm.name !== dirName) fail(`${path}: frontmatter name "${fm.name}" does not match directory`);
  if (typeof fm.description !== 'string' || !fm.description.trim()) fail(`${path}: missing description`);
  const md = fm.metadata || {};
  if (typeof md.version !== 'string') fail(`${path}: metadata.version missing or not a string`);

  let group; let phase = null;
  if (md.phase !== undefined) {
    if (!PHASES.includes(md.phase)) fail(`${path}: unknown phase "${md.phase}"`);
    group = 'phase';
    phase = md.phase;
  } else if (['foundation', 'utility', 'tool'].includes(md.classification)) {
    group = md.classification;
  } else {
    fail(`${path}: neither metadata.phase nor a known metadata.classification`);
  }

  const entry = {
    name: dirName,
    description: fm.description,
    version: md.version,
    group,
    phase,
    family: typeof md.tool === 'string' ? md.tool : null,
    move: typeof md.move === 'string' ? md.move : null,
    path: `skills/${dirName}`,
    references: facts.references,
  };
  if (facts.sample) entry.sample = facts.sample;
  return entry;
}

export function buildManifest(repo) {
  const skillDirs = readdirSync(join(repo, 'skills'), { withFileTypes: true })
    .filter((d) => d.isDirectory()).map((d) => d.name).sort();
  const entries = skillDirs.map((name) => {
    const skillPath = join(repo, 'skills', name, 'SKILL.md');
    if (!existsSync(skillPath)) fail(`skills/${name}: no SKILL.md`);
    const references = ['TEMPLATE.md', 'EXAMPLE.md']
      .filter((r) => existsSync(join(repo, 'skills', name, 'references', r)));
    const sampleDir = join(repo, 'library', 'skill-output-samples', name);
    const facts = { references, sample: existsSync(sampleDir) ? `library/skill-output-samples/${name}` : null };
    return buildEntry(name, readFileSync(skillPath, 'utf8'), facts);
  });

  const countMd = (dir, excludeReadme) => {
    const p = join(repo, dir);
    if (!existsSync(p)) return 0;
    return readdirSync(p).filter((f) => f.endsWith('.md') && (!excludeReadme || f !== 'README.md')).length;
  };
  const byGroup = (g) => entries.filter((e) => e.group === g).length;

  return {
    schema: 1,
    generated_by: 'scripts/gen-skill-manifest.mjs',
    catalog: {
      skills: entries.length,
      phase: byGroup('phase'),
      foundation: byGroup('foundation'),
      utility: byGroup('utility'),
      tool: byGroup('tool'),
      sub_agents: countMd('agents', false),
      commands: countMd('commands', false),
      workflows: countMd('_workflows', true),
    },
    entries,
  };
}

export function renderManifest(manifest) {
  return `${JSON.stringify(manifest, null, 2)}\n`;
}

/** Entry heading: tool entries keep the full name; others drop the group/phase prefix. */
export function headingName(e) {
  if (e.group === 'tool') return e.name;
  const prefix = `${e.group === 'phase' ? e.phase : e.group}-`;
  return e.name.startsWith(prefix) ? e.name.slice(prefix.length) : e.name;
}

function renderEntry(e, level) {
  return `${'#'.repeat(level)} ${headingName(e)}\n**Path:** \`${e.path}/SKILL.md\`\n\n${e.description}\n`;
}

export function renderAgentsBlock(manifest) {
  const es = manifest.entries;
  const out = [];
  const section = (title, body) => out.push(`### ${title}\n\n${body.join('\n')}`);

  const foundation = es.filter((e) => e.group === 'foundation').map((e) => renderEntry(e, 4));
  foundation.push(`${FOUNDATION_NOTE}\n`);
  section('Foundation Classification', foundation);

  for (const phase of PHASES) {
    const title = `${phase[0].toUpperCase()}${phase.slice(1)} Phase`;
    section(title, es.filter((e) => e.phase === phase).map((e) => renderEntry(e, 4)));
  }

  section('Utility Skills', es.filter((e) => e.group === 'utility').map((e) => renderEntry(e, 4)));

  const tools = es.filter((e) => e.group === 'tool');
  const toolBody = [`${TOOL_INTRO}\n`];
  for (const e of tools.filter((t) => t.family === null)) toolBody.push(renderEntry(e, 4));
  const families = [...new Set(tools.filter((t) => t.family !== null).map((t) => t.family))];
  for (const f of families) {
    if (!FAMILY_ORDER.includes(f)) fail(`unknown tool family "${f}": register it in FAMILY_ORDER / MOVE_ORDER / FAMILY_INTROS (gen-skill-manifest.mjs)`);
  }
  for (const f of FAMILY_ORDER) {
    const members = tools.filter((t) => t.family === f);
    if (!members.length) continue;
    const order = MOVE_ORDER[f];
    members.sort((a, b) => order.indexOf(a.move) - order.indexOf(b.move));
    toolBody.push(`#### ${FAMILY_INTROS[f].title}\n\n${FAMILY_INTROS[f].intro}\n`);
    for (const e of members) toolBody.push(renderEntry(e, 5));
  }
  section('Tool Classification', toolBody);

  return `${out.join('\n---\n\n')}\n---\n`;
}

/** Splice the generated block between the markers. Refuses (loudly) without both markers. */
export function spliceAgents(agentsText, block) {
  const s = agentsText.indexOf(MARKER_START);
  const e = agentsText.indexOf(MARKER_END);
  if (s === -1 || e === -1 || e < s) fail('AGENTS.md: marker pair not found (skills-catalog:start/end); refusing to write');
  return `${agentsText.slice(0, s + MARKER_START.length)}\n${block}${agentsText.slice(e)}`;
}

function main() {
  const repo = join(dirname(fileURLToPath(import.meta.url)), '..');
  const args = process.argv.slice(2);
  const check = args.includes('--check');
  const agents = args.includes('--agents');
  const manifest = buildManifest(repo);

  if (!agents) {
    const target = join(repo, 'skill-manifest.json');
    const rendered = renderManifest(manifest);
    const current = existsSync(target) ? readFileSync(target, 'utf8') : null;
    if (check) {
      if (current === rendered) { console.log('skill-manifest.json is current.'); return; }
      console.error('STALE  skill-manifest.json does not match the catalog. Run: node scripts/gen-skill-manifest.mjs');
      process.exit(1);
    }
    writeFileSync(target, rendered);
    console.log(`skill-manifest.json written (${manifest.catalog.skills} skills).`);
    return;
  }

  const target = join(repo, 'AGENTS.md');
  const currentText = readFileSync(target, 'utf8');
  const next = spliceAgents(currentText, renderAgentsBlock(manifest));
  if (check) {
    if (currentText === next) { console.log('AGENTS.md skills catalog is current.'); return; }
    console.error('STALE  AGENTS.md skills-catalog block does not match the catalog. Run: node scripts/gen-skill-manifest.mjs --agents');
    process.exit(1);
  }
  writeFileSync(target, next);
  console.log('AGENTS.md skills-catalog block rewritten.');
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) main();
